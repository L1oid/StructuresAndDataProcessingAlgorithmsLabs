class BinHeap:
    def __init__(self):
        self.heapList = [0]
        self.currentSize = 0

    def percUp(self, i):
        while i // 2 > 0:
            if self.heapList[i] < self.heapList[i // 2]:
                tmp = self.heapList[i // 2]
                self.heapList[i // 2] = self.heapList[i]
                self.heapList[i] = tmp
            i = i // 2

    def insert(self, k):
        self.heapList.append(k)
        self.currentSize = self.currentSize + 1
        self.percUp(self.currentSize)

    def percDown(self, i):
        while (i * 2) <= self.currentSize:
            mc = self.minChild(i)
            if self.heapList[i] > self.heapList[mc]:
                tmp = self.heapList[i]
                self.heapList[i] = self.heapList[mc]
                self.heapList[mc] = tmp
            i = mc

    def minChild(self, i):
        if i * 2 + 1 > self.currentSize:
            return i * 2
        else:
            if self.heapList[i * 2] < self.heapList[i * 2 + 1]:
                return i * 2
            else:
                return i * 2 + 1

    def delMin(self):
        retval = self.heapList[1]
        self.heapList[1] = self.heapList[self.currentSize]
        self.currentSize = self.currentSize - 1
        self.heapList.pop()
        self.percDown(1)
        return retval

    def buildHeap(self, alist):
        i = len(alist) // 2
        self.currentSize = len(alist)
        self.heapList = [0] + alist[:]
        while (i > 0):
            self.percDown(i)
            i = i - 1


class PriorityQueue:
    def __init__(self, max_priority):
        self._heaps = [BinHeap() for _ in range(max_priority)]

    def enqueue(self, priority, data):
        self._heaps[priority - 1].insert(data)

    def dequeue(self):
        for i in range(len(self._heaps) - 1, -1, -1):
            if self._heaps[i].currentSize > 0:
                return self._heaps[i].delMin()


pr = PriorityQueue(5)
pr.enqueue(5, 1)
pr.enqueue(1, 14)
pr.enqueue(2, 7)
pr.enqueue(3, 12)
pr.enqueue(5, 2)

for i in range(5):
    print(pr.dequeue())
